package practice;
public class Pre_And_Post_Incremention_Of_Variable 
{
  public static void main(String args[])
  {
	  int a=5;
	  int b=++a;
	  System.out.print(a+"\n");
	  System.out.print(b+"\n");
	  int i=2;
	  int j=i++;
	  System.out.print(i+"\n");
	  System.out.print(j+"\n");
	  int k=1;
	  k=k++;
	  System.out.print(k);
  }
}
